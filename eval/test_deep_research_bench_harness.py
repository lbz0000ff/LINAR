def test_production_runner_initializes_agent_like_gui_then_executes_in_workspace(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    import agent as agent_module
    import skill as skill_module
    import tool_registry as tool_registry_module

    workspace = tmp_path / "task"
    workspace.mkdir()
    calls: dict[str, object] = {}

    class FakeSkill:
        def detach_runtime(self, agent: object) -> None:
            calls["detached"] = agent

    fake_skill = FakeSkill()

    class FakeAgent:
        def __init__(self, tools: dict, memory_enabled: bool) -> None:
            calls["init_cwd"] = Path.cwd()
            calls["memory_enabled"] = memory_enabled
            self.hooks = SimpleNamespace(
                unregister=lambda name: calls.setdefault("unregistered_hooks", []).append(name)
            )
            self.tools = tools
            self.chat_history: list[dict] = []
            self._conversation_round = 0
            self._workspace_root = None
            self.emit = None

        async def process_with_llm(self) -> None:
            calls["process_cwd"] = Path.cwd()
            calls["history"] = list(self.chat_history)
            calls["subagent_tool_factory"] = self._subagent_tool_factory

    monkeypatch.setattr(tool_registry_module, "get_tools", lambda sets, include_mcp: {
        "selected_sets": sets,
        "include_mcp": include_mcp,
    })
    monkeypatch.setattr(agent_module, "Agent", FakeAgent)
    monkeypatch.setattr(skill_module, "load_skills_from_markdown", lambda path: calls.setdefault("skills_path", path))
    monkeypatch.setattr(skill_module, "get_skill", lambda name: fake_skill if name == "deep-research" else None)

    def fake_activate(agent: FakeAgent, skill: FakeSkill, **kwargs: object) -> None:
        calls["activated"] = (agent, skill, kwargs)

    monkeypatch.setattr(skill_module, "activate_skill_for_agent", fake_activate)

    previous_cwd = Path.cwd()
    try:
        os.chdir(workspace)
        asyncio.run(harness.run_deep_research("benchmark prompt", workspace))
    finally:
        os.chdir(previous_cwd)

    assert calls["init_cwd"] == harness.AGENT_DIR
    assert calls["process_cwd"] == workspace
    assert calls["memory_enabled"] is False
    assert calls["unregistered_hooks"] == [
        "builtin_db_user_persist",
        "builtin_db_agent_persist",
        "builtin_db_tool_persist",
    ]
    assert calls["skills_path"] == str(harness.ROOT_DIR / "skills")
    assert calls["history"][-1] == {
        "role": "user",
        "content": "benchmark prompt",
        "round": 1,
    }
    assert calls["detached"] is calls["activated"][0]
    assert callable(calls["subagent_tool_factory"])


def test_benchmark_tools_block_paths_outside_task_workspace(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    import tool_registry as tool_registry_module

    workspace = tmp_path / "task"
    workspace.mkdir()
    outside = tmp_path / "reference.jsonl"
    outside.write_text("secret reference answer", encoding="utf-8")
    calls: list[dict] = []

    class FakeTool:
        def __init__(self, name: str) -> None:
            self.name = name
            self.tool_schema = {"name": name}

        def execute(self, **kwargs: object) -> dict:
            calls.append({"name": self.name, **kwargs})
            return {"ok": True}

    available = {
        name: FakeTool(name)
        for name in (
            "read_file", "write_file", "search_files",
            "cmd_execute", "create_workspace", "switch_workspace",
            "mcp_stepsearch_web_search", "mcp_stepsearch_web_fetch",
            "mcp_anysearch_search", "mcp_anysearch_batch_search",
            "mcp_anysearch_web_fetch",
            "mcp_playwright_browser_navigate",
        )
    }
    requested: dict[str, object] = {}

    def fake_get_tools(sets: list[str], include_mcp: bool) -> dict:
        requested["sets"] = sets
        requested["include_mcp"] = include_mcp
        return available

    monkeypatch.setattr(tool_registry_module, "get_tools", fake_get_tools)

    tools = harness.build_benchmark_tools(workspace)

    assert requested == {
        "sets": ["time", "file", "web", "plan", "vision"],
        "include_mcp": True,
    }
    assert "cmd_execute" not in tools
    assert "create_workspace" not in tools
    assert "switch_workspace" not in tools
    assert "mcp_stepsearch_web_search" in tools
    assert "mcp_stepsearch_web_fetch" in tools
    assert "mcp_anysearch_web_fetch" in tools
    assert "mcp_anysearch_search" in tools
    assert "mcp_anysearch_batch_search" in tools
    assert "mcp_playwright_browser_navigate" not in tools
    assert "error" in tools["read_file"].execute(file_path=str(outside))
    assert "error" in tools["search_files"].execute(pattern="secret", path=str(tmp_path))
    assert calls == []

    inside = workspace / "notes.md"
    assert tools["write_file"].execute(file_path=str(inside), content="ok") == {"ok": True}
    assert calls == [{"name": "write_file", "file_path": str(inside), "content": "ok"}]
