def test_load_report_returns_exact_markdown(tmp_path: Path) -> None:
    article = "# Final report\n\nEvidence with [a citation](https://example.com).\n"
    (tmp_path / "report.md").write_text(article, encoding="utf-8")

    assert harness.load_report(tmp_path) == article


@pytest.mark.parametrize("article", [None, "   \n\t"])
def test_load_report_rejects_missing_or_empty_files(tmp_path: Path, article: str | None) -> None:
    if article is not None:
        (tmp_path / "report.md").write_text(article, encoding="utf-8")

    with pytest.raises(harness.BenchmarkRunError, match="report.md"):
        harness.load_report(tmp_path)


