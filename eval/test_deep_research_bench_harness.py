def test_write_results_uses_required_jsonl_schema(tmp_path: Path) -> None:
    output = tmp_path / "linar.jsonl"
    result = harness.BenchRunResult(
        id=1,
        prompt="hello",
        article="# Report\n\nUseful article with citations.",
        ok=True,
    )

    harness.write_results(output, [result], strict=True)

    rows = [json.loads(line) for line in output.read_text(encoding="utf-8").splitlines()]
    assert rows == [
        {
            "id": 1,
            "prompt": "hello",
            "article": "# Report\n\nUseful article with citations.",
        }
    ]


